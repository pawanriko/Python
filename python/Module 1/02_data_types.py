# Integer
age = 20

# Float
cgpa = 9.0

# String
college = "IITM College of Engineering"

# Boolean
is_learning_python = True

print("=== Data Types in Python ===\n")

print(f"Age: {age}")
print(f"Type: {type(age)}\n")

print(f"CGPA: {cgpa}")
print(f"Type: {type(cgpa)}\n")

print(f"College: {college}")
print(f"Type: {type(college)}\n")

print(f"Learning Python: {is_learning_python}")
print(f"Type: {type(is_learning_python)}")
